// Helper functions
export const getAccountByCode = (code: string): EgyptianAccountTemplate | undefined => {
  return EGYPTIAN_CHART_OF_ACCOUNTS.find((acc) => acc.code === code);
};

export const getAccountsByType = (
  type: 'Asset' | 'Liability' | 'Equity' | 'Revenue' | 'Expense'
): EgyptianAccountTemplate[] => {
  return EGYPTIAN_CHART_OF_ACCOUNTS.filter((acc) => acc.type === type);
};

export const getAccountsByCategory = (category: string): EgyptianAccountTemplate[] => {
  return EGYPTIAN_CHART_OF_ACCOUNTS.filter((acc) => acc.category === category);
};

export const searchAccounts = (searchTerm: string, language: 'en' | 'ar' = 'en'): EgyptianAccountTemplate[] => {
  const term = searchTerm.toLowerCase();
  return EGYPTIAN_CHART_OF_ACCOUNTS.filter((acc) => {
    if (language === 'ar') {
      return acc.nameAr.includes(searchTerm) || acc.code.includes(term);
    }
    return acc.nameEn.toLowerCase().includes(term) || acc.code.includes(term);
  });
};

